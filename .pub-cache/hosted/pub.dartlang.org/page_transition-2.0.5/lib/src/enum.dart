/// Transition enum
// ignore_for_file: public_member_api_docs

enum PageTransitionType {
  /// Fade Animation
  fade,

  /// Right to left animation
  rightToLeft,

  /// Left to right animation
  leftToRight,

  /// Top the bottom animation

  topToBottom,

  /// bottom the top animation

  bottomToTop,

  /// scale animation

  scale,

  /// Rotate animation

  rotate,

  /// Size animation

  size,

  /// Right to left with fading animation

  rightToLeftWithFade,

  /// Left to right with fading animation

  leftToRightWithFade,

  /// Left to right slide as if joined

  leftToRightJoined,

  /// Right to left slide as if joined

  rightToLeftJoined,

  /// Top to Bottom slide as if joined
  topToBottomJoined,

  /// Bottom to Top slide as if joined
  bottomToTopJoined,
}
